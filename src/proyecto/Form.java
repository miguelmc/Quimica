package proyecto;

public class Form {
	int batch;
	String producto;
	
	public Form(){
		batch = 0;
		producto = "";
	}

	public int getBatch() {
		return batch;
	}

	public void setBatch(int batch) {
		this.batch = batch;
	}

	public String getProducto() {
		return producto;
	}

	public void setProducto(String producto) {
		this.producto = producto;
	}
	
	
}
